def get_cost(tree, Path) -> int:
    """Lo que hace esta función es calcular el costo de un camino dado el árbol, 
    para poder comparar los resultados entre los distintos algoritmos"""
    
    cost = 0
    if type(Path) == tuple:
        path = Path[0].copy()
    else:
        path = Path.copy()

    if path == []:
        return cost

    node = path.pop(0)
    while path:
        for weights in tree[1]:
            if weights[0] != node:
                continue
            for connections in weights[1]:
                if connections[0] != path[0]:
                    continue
                cost += connections[1]
                break
            break
        node = path.pop(0)
    return cost

def measure_time(f, *args):
    """Lo que importa de aqui es el hecho que **kwargs guarda el los inputs de la función f,
     lo cual es muy util en caso de que los algoritmos de busqueda que se miden tengan distinto numero de algoritmos
     ademas, el tiempo que saca esta en ms"""
    start_time = time()
    val = f(*args)
    end_time = time()
    if val is not None:
        return (val, (end_time - start_time)*(10 ** 3))
    return (end_time - start_time)*(10 ** 3)

